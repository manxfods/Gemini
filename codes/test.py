# Um simples programa em Python
# Solicitar ao usuário que insira seu nome
nome = input("Digite seu nome: ")

# Solicitar ao usuário que insira sua idade
idade = input("Digite sua idade: ")

# Converter a entrada de idade para um número inteiro
idade = int(idade)

# Imprimir uma mensagem de saudação com o nome e idade do usuário
print(f"Olá, {nome}! Você tem {idade} anos de idade.")
